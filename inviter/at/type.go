// It creates a new client for the given URL.
//
// Args:
//   url (string): The address of the RPC server.
func NewCli(url string) *jsonrpc.Client {
	cli, err := jsonrpc.NewClient(url)
	if err != nil {
		panic(fmt.Sprintf("无法连接到地址:%s", url))
	}
	return cli
}

// > It takes a string as input, and returns an ABI object
//
// Args:
//   s (string): abi content
func NewAbi(s string) *abi.ABI {
	a, err := abi.NewABI(s)
	if err != nil {
		panic("无法读取abi内容")
	}
	return a
}

// `NewRelation` is a constructor function that returns a pointer to a `Relation` struct
//
// Args:
//   addr (string): the address of the contract
func NewRelation(addr string) *Relation {
	return &Relation{
		Addr:     addr,
		Contract: contract.NewContract(web3.HexToAddress(addr), NewAbi(RelationABI), w3),
	}
}

// `NewTeam` is a function that takes a string as an argument and returns a pointer to a `Team` struct
//
// Args:
//   addr (string): the address of the contract
func NewTeam(addr string) *Team {
	return &Team{
		Addr:     addr,
		Contract: contract.NewContract(web3.HexToAddress(addr), NewAbi(TeamABI), w3),
	}
}

// `NewOrder` is a constructor function that returns a pointer to an `Order` struct
//
// Args:
//   addr (string): the contract address
func NewOrder(addr string) *Order {
	return &Order{
		Addr:     addr,
		Contract: contract.NewContract(web3.HexToAddress(addr), NewAbi(OderABI), w3),
	}
}

// Get the current block number from the Ethereum blockchain.
func NowBlock() web3.BlockNumber {
	blockNumber, _ := w3.Eth().BlockNumber()
	return web3.BlockNumber(blockNumber)
}

